Повышаться цены на "свет" будут и в 2017 году – в марте электроэнергия подорожает вновь. Но это будет уже последним – пятым – этапом роста тарифов.
График повышения НКРЭКУ утвердил еще в начале 2015 года, и, по расчетам властей, весной 2017 года стоимость электричества для населения достигнет рыночного уровня.